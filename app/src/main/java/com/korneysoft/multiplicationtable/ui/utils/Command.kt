package com.korneysoft.multiplicationtable.ui.utils

enum class Command {
    PROCESS_START,
    PROCESS_STOP,
    PROCESS_FINISH,

    TASK_START,
    TASK_STOP,
    TASK_FINISH;

    companion object {
        fun getCommandPair(command: Command, value: Int? = null): Pair<Command, Int?> {
            return Pair(command, value)
        }
    }
}
